function addpionblanc() {
    for (var i = 1; i < 9; i++) {
        for (var j = 1; j < 9; j++) {

            var num = 0;
            num = 10 * i + j;
            var id = num.toString();
            document.getElementById(id).classList.add('pionblancadd');

        }
    }
}

function movepionblanc() {
    for (var i = 1; i < 9; i++) {
        for (var j = 1; j < 9; j++) {

            var num = 0;
            num = 10 * i + j;
            var id = num.toString();

            document.getElementById(id).classList.remove('pionblancadd');


        }
    }
}

